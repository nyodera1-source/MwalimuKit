export function generatePerformanceProgramPdf(
  data: ProgramExportData
): ArrayBuffer {
  const doc = createDoc();
  const pageWidth = getPageWidth(doc);
  const contentWidth = getContentWidth(doc);
  const fd = data.formData;
  let y = MARGIN;

  // ─── Cover Section ───
  y += 20;

  // Event name (large font)
  doc.setFontSize(22);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  const eventLines = doc.splitTextToSize(fd.eventName, contentWidth - 10);
  doc.text(eventLines, pageWidth / 2, y, { align: "center" });
  y += eventLines.length * 10 + 6;

  // Decorative line
  doc.setDrawColor(...HEADER_COLOR);
  doc.setLineWidth(0.5);
  doc.line(pageWidth / 2 - 40, y, pageWidth / 2 + 40, y);
  doc.setLineWidth(0.2);
  y += 8;

  // Venue
  doc.setFontSize(12);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(60, 60, 60);
  doc.text(fd.venue, pageWidth / 2, y, { align: "center" });
  y += 7;

  // Date & Time
  doc.setFontSize(11);
  doc.text(`${fd.eventDate}  |  ${fd.eventTime}`, pageWidth / 2, y, {
    align: "center",
  });
  y += 8;

  // School name
  if (data.schoolName) {
    doc.setFontSize(10);
    doc.setFont("helvetica", "italic");
    doc.setTextColor(80, 80, 80);
    doc.text(data.schoolName, pageWidth / 2, y, { align: "center" });
    y += 7;
  }

  doc.setTextColor(0, 0, 0);
  y += 10;

  // ─── Program Header with decorative line ───
  doc.setFontSize(14);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("PROGRAM", pageWidth / 2, y, { align: "center" });
  y += 3;
  doc.setDrawColor(...HEADER_COLOR);
  doc.setLineWidth(0.4);
  doc.line(MARGIN + 20, y, pageWidth - MARGIN - 20, y);
  doc.setLineWidth(0.2);
  doc.setTextColor(0, 0, 0);
  y += 8;

  // ─── Program Items Table ───
  const colOrder = MARGIN;
  const colTitle = MARGIN + 12;
  const colPerformer = MARGIN + 82;
  const colDuration = pageWidth - MARGIN - 20;

  // Table header
  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...HEADER_COLOR);
  doc.rect(MARGIN, y, contentWidth, 7, "F");
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(255, 255, 255);
  doc.text("#", colOrder + 2, y + 5);
  doc.text("Item", colTitle + 2, y + 5);
  doc.text("Performer / Group", colPerformer + 2, y + 5);
  doc.text("Duration", colDuration + 2, y + 5);
  doc.setTextColor(0, 0, 0);
  y += 9;

  // Build a combined list of items and intermissions, sorted by order
  const intermissionSet = new Set(
    fd.intermissions.map((i) => i.afterItemNumber)
  );
  const intermissionMap = new Map(
    fd.intermissions.map((i) => [i.afterItemNumber, i])
  );

  fd.programItems.forEach((item, idx) => {
    const titleText = doc.splitTextToSize(item.title, 66);
    const performerText = doc.splitTextToSize(
      item.performerOrGroup,
      pageWidth - MARGIN - colPerformer - 24
    );
    const rowH =
      Math.max(titleText.length, performerText.length) * LINE_HEIGHT + 3;

    y = checkPageBreak(doc, y, rowH);
    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }
    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(String(item.order), colOrder + 3, y + 3);
    doc.setFont("helvetica", "bold");
    doc.text(titleText, colTitle + 2, y + 3);
    doc.setFont("helvetica", "normal");
    doc.text(performerText, colPerformer + 2, y + 3);
    doc.text(`${item.duration} min`, colDuration + 2, y + 3);
    y += rowH;

    // Check for intermission after this item
    if (intermissionSet.has(item.order)) {
      const inter = intermissionMap.get(item.order)!;
      const interH = 8;
      y = checkPageBreak(doc, y, interH);
      doc.setFillColor(243, 232, 255);
      doc.rect(MARGIN, y - 1, contentWidth, interH, "F");
      doc.setFontSize(9);
      doc.setFont("helvetica", "bold");
      doc.setTextColor(...SECTION_COLOR);
      doc.text(
        `--- INTERMISSION (${inter.duration} min) ---`,
        pageWidth / 2,
        y + 5,
        { align: "center" }
      );
      doc.setTextColor(0, 0, 0);
      y += interH;
    }
  });
  y += 6;

  // ─── Acknowledgments ───
  if (fd.acknowledgments) {
    y = drawSectionHeader(doc, y, "ACKNOWLEDGMENTS");
    y = drawWrappedText(doc, y, fd.acknowledgments);
  }

  // ─── Special Notes ───
  if (fd.specialNotes) {
    y = drawSectionHeader(doc, y, "NOTES");
    y = drawWrappedText(doc, y, fd.specialNotes);
  }

  // ─── Footer ───
  drawFooter(doc, "Performance Program");

  return doc.output("arraybuffer");
}

