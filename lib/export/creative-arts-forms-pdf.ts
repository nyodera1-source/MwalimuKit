export function generateRehearsalPlanPdf(
  data: RehearsalExportData
): ArrayBuffer {
  const doc = createDoc();
  const pageWidth = getPageWidth(doc);
  const contentWidth = getContentWidth(doc);
  let y = MARGIN;

  // ─── School Name ───
  if (data.schoolName) {
    doc.setFontSize(11);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(40, 40, 40);
    doc.text(data.schoolName.toUpperCase(), pageWidth / 2, y, {
      align: "center",
    });
    y += 7;
  }

  // ─── Title ───
  doc.setFontSize(16);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("REHEARSAL / PRACTICE PLAN", pageWidth / 2, y, {
    align: "center",
  });
  doc.setTextColor(0, 0, 0);
  y += 6;

  doc.setFontSize(11);
  doc.setFont("helvetica", "normal");
  const titleLines = doc.splitTextToSize(data.formName, contentWidth);
  doc.text(titleLines, pageWidth / 2, y, { align: "center" });
  y += titleLines.length * 5 + 6;

  // ─── Info Box ───
  y = checkPageBreak(doc, y, 20);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 16, "F");
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  doc.text(
    `Ensemble: ${data.formData.ensembleType}`,
    MARGIN + 3,
    y + 5
  );
  doc.text(
    `Duration: ${data.formData.duration} minutes`,
    pageWidth / 2 + 5,
    y + 5
  );
  doc.text(
    `Date: ${data.date || "_______________"}`,
    MARGIN + 3,
    y + 12
  );
  doc.text(
    `Teacher: ${data.teacherName || "_______________"}`,
    pageWidth / 2 + 5,
    y + 12
  );
  y += 20;

  // ─── Objectives ───
  y = drawSectionHeader(doc, y, "OBJECTIVES");
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  data.formData.rehearsalObjectives.forEach((obj, idx) => {
    const text = `${idx + 1}. ${obj}`;
    const lines = doc.splitTextToSize(text, contentWidth - 8);
    y = checkPageBreak(doc, y, lines.length * LINE_HEIGHT + 2);
    doc.text(lines, MARGIN + 3, y + 2);
    y += lines.length * LINE_HEIGHT + 1;
  });
  y += 4;

  // ─── Warm-Up Activities Table ───
  y = drawSectionHeader(doc, y, "WARM-UP ACTIVITIES");

  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 7, "F");
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("Activity", MARGIN + 3, y + 5);
  doc.text("Duration", pageWidth - MARGIN - 25, y + 5);
  doc.setTextColor(0, 0, 0);
  y += 9;

  data.formData.warmUpActivities.forEach((act, idx) => {
    const lines = doc.splitTextToSize(act.activity, contentWidth - 35);
    const rowH = lines.length * LINE_HEIGHT + 3;
    y = checkPageBreak(doc, y, rowH);
    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }
    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(lines, MARGIN + 3, y + 3);
    doc.text(`${act.duration} min`, pageWidth - MARGIN - 23, y + 3);
    y += rowH;
  });
  y += 4;

  // ─── Repertoire Table ───
  y = drawSectionHeader(doc, y, "REPERTOIRE");

  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 7, "F");
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  const repColTitle = MARGIN;
  const repColComposer = MARGIN + 65;
  const repColFocus = MARGIN + 115;
  doc.text("Title", repColTitle + 3, y + 5);
  doc.text("Composer", repColComposer + 3, y + 5);
  doc.text("Focus Area", repColFocus + 3, y + 5);
  doc.setTextColor(0, 0, 0);
  y += 9;

  data.formData.repertoire.forEach((item, idx) => {
    const titleText = doc.splitTextToSize(item.title, 60);
    const focusText = doc.splitTextToSize(item.focus, contentWidth - 118);
    const rowH =
      Math.max(titleText.length, focusText.length) * LINE_HEIGHT + 3;
    y = checkPageBreak(doc, y, rowH);
    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }
    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(titleText, repColTitle + 3, y + 3);
    doc.text(item.composer || "-", repColComposer + 3, y + 3);
    doc.text(focusText, repColFocus + 3, y + 3);
    y += rowH;
  });
  y += 4;

  // ─── Section Focus ───
  y = drawSectionHeader(doc, y, "SECTION FOCUS");

  data.formData.sectionFocus.forEach((section) => {
    y = checkPageBreak(doc, y, 15);
    doc.setFontSize(9);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(...SECTION_COLOR);
    doc.text(section.section, MARGIN + 3, y + 2);
    doc.setTextColor(0, 0, 0);
    y += 6;

    // Goals
    if (section.goals.length > 0) {
      doc.setFontSize(8);
      doc.setFont("helvetica", "bold");
      doc.text("Goals:", MARGIN + 5, y + 2);
      y += 5;
      doc.setFont("helvetica", "normal");
      section.goals.forEach((goal) => {
        const text = `\u2022 ${goal}`;
        const lines = doc.splitTextToSize(text, contentWidth - 14);
        y = checkPageBreak(doc, y, lines.length * LINE_HEIGHT + 1);
        doc.text(lines, MARGIN + 8, y + 2);
        y += lines.length * LINE_HEIGHT + 1;
      });
    }

    // Exercises
    if (section.exercises.length > 0) {
      doc.setFontSize(8);
      doc.setFont("helvetica", "bold");
      doc.text("Exercises:", MARGIN + 5, y + 2);
      y += 5;
      doc.setFont("helvetica", "normal");
      section.exercises.forEach((ex) => {
        const text = `\u2022 ${ex}`;
        const lines = doc.splitTextToSize(text, contentWidth - 14);
        y = checkPageBreak(doc, y, lines.length * LINE_HEIGHT + 1);
        doc.text(lines, MARGIN + 8, y + 2);
        y += lines.length * LINE_HEIGHT + 1;
      });
    }
    y += 4;
  });

  // ─── Cool Down ───
  y = drawSectionHeader(doc, y, "COOL DOWN");
  y = drawWrappedText(doc, y, data.formData.coolDown);

  // ─── Notes for Next Session ───
  y = drawSectionHeader(doc, y, "NOTES FOR NEXT SESSION");
  y = drawWrappedText(doc, y, data.formData.notesForNextSession);

  // ─── Footer ───
  drawFooter(doc, "Rehearsal Plan");

  return doc.output("arraybuffer");
}

