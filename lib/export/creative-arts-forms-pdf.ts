// 4. PORTFOLIO ASSESSMENT SHEET
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

export function generatePortfolioAssessmentPdf(
  data: PortfolioExportData
): ArrayBuffer {
  const doc = createDoc();
  const pageWidth = getPageWidth(doc);
  const contentWidth = getContentWidth(doc);
  const fd = data.formData;
  let y = MARGIN;

  // ─── School Name ───
  if (data.schoolName) {
    doc.setFontSize(11);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(40, 40, 40);
    doc.text(data.schoolName.toUpperCase(), pageWidth / 2, y, {
      align: "center",
    });
    y += 7;
  }

  // ─── Title ───
  doc.setFontSize(16);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("PORTFOLIO ASSESSMENT SHEET", pageWidth / 2, y, {
    align: "center",
  });
  doc.setTextColor(0, 0, 0);
  y += 6;

  doc.setFontSize(11);
  doc.setFont("helvetica", "normal");
  const titleLines = doc.splitTextToSize(data.formName, contentWidth);
  doc.text(titleLines, pageWidth / 2, y, { align: "center" });
  y += titleLines.length * 5 + 6;

  // ─── Student Info ───
  y = checkPageBreak(doc, y, 22);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 18, "F");
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  doc.text(
    `Student Name: ${data.studentName || "______________________________"}`,
    MARGIN + 3,
    y + 6
  );
  doc.text(
    `Grade: ${data.grade}`,
    pageWidth / 2 + 10,
    y + 6
  );
  doc.text(
    `Date: ${data.date || "_______________"}`,
    MARGIN + 3,
    y + 14
  );
  doc.text(
    `Teacher: ${data.teacherName || "_______________"}`,
    pageWidth / 2 + 10,
    y + 14
  );
  y += 22;

  // ─── Portfolio Type ───
  doc.setFontSize(9);
  doc.setFont("helvetica", "bold");
  doc.text(`Portfolio Type: `, MARGIN + 3, y);
  doc.setFont("helvetica", "normal");
  doc.text(fd.portfolioType, MARGIN + 35, y);
  y += 7;

  // ─── Required Components (Checklist) ───
  y = drawSectionHeader(doc, y, "REQUIRED COMPONENTS");

  fd.requiredComponents.forEach((comp, idx) => {
    const descLines = doc.splitTextToSize(
      `${comp.component} - ${comp.description}`,
      contentWidth - 14
    );
    const rowH = descLines.length * LINE_HEIGHT + 3;
    y = checkPageBreak(doc, y, rowH);

    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }

    // Checkbox square
    doc.setDrawColor(180, 180, 180);
    doc.rect(MARGIN + 3, y + 0.5, 4, 4);

    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(descLines, MARGIN + 10, y + 3);
    y += rowH;
  });
  y += 4;

  // ─── Assessment Criteria ───
  y = drawSectionHeader(doc, y, "ASSESSMENT CRITERIA");

  fd.assessmentCriteria.forEach((crit) => {
    // Criterion name
    y = checkPageBreak(doc, y, 15);
    doc.setFontSize(9);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(...SECTION_COLOR);
    doc.text(crit.criterion, MARGIN + 3, y + 2);
    doc.setTextColor(0, 0, 0);
    y += 7;

    // Rubric levels table header
    const colLevel = MARGIN + 2;
    const colDesc = MARGIN + 35;
    const colPoints = pageWidth - MARGIN - 28;
    const colCheck = pageWidth - MARGIN - 12;

    y = checkPageBreak(doc, y, 10);
    doc.setFillColor(...TABLE_HEADER_BG);
    doc.rect(MARGIN, y, contentWidth, 6, "F");
    doc.setFontSize(7);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(...HEADER_COLOR);
    doc.text("Level", colLevel, y + 4);
    doc.text("Description", colDesc, y + 4);
    doc.text("Points", colPoints, y + 4);
    doc.text("Check", colCheck, y + 4);
    doc.setTextColor(0, 0, 0);
    y += 8;

    // Rubric rows
    crit.rubricLevels.forEach((level, lIdx) => {
      const descText = doc.splitTextToSize(
        level.description,
        colPoints - colDesc - 4
      );
      const rowH = descText.length * LINE_HEIGHT + 3;
      y = checkPageBreak(doc, y, rowH);

      if (lIdx % 2 === 0) {
        doc.setFillColor(...ALT_ROW_BG);
        doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
      }

      doc.setFontSize(8);
      doc.setFont("helvetica", "bold");
      doc.text(level.level, colLevel, y + 3);
      doc.setFont("helvetica", "normal");
      doc.text(descText, colDesc, y + 3);
      doc.text(String(level.pointValue), colPoints + 4, y + 3);

      // Check box
      doc.setDrawColor(180, 180, 180);
      doc.rect(colCheck + 1, y + 0.5, 4, 4);

      y += rowH;
    });
    y += 4;
  });

  // ─── Score Summary ───
  y = drawSectionHeader(doc, y, "SCORE SUMMARY");

  // Header row
  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 6, "F");
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("Criterion", MARGIN + 3, y + 4);
  doc.text("Score", pageWidth - MARGIN - 20, y + 4);
  doc.setTextColor(0, 0, 0);
  y += 8;

  fd.assessmentCriteria.forEach((crit, idx) => {
    const rowH = 7;
    y = checkPageBreak(doc, y, rowH);
    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }
    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(crit.criterion, MARGIN + 3, y + 4);
    // Blank score box
    doc.setDrawColor(180, 180, 180);
    doc.rect(pageWidth - MARGIN - 18, y, 14, 6);
    y += rowH;
  });

  // Total row
  y = checkPageBreak(doc, y, 8);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 8, "F");
  doc.setFontSize(9);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("TOTAL", MARGIN + 3, y + 6);
  doc.setDrawColor(...SECTION_COLOR);
  doc.rect(pageWidth - MARGIN - 18, y + 1, 14, 6);
  doc.setFontSize(7);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(120, 120, 120);
  doc.text(`/ ${fd.totalPoints}`, pageWidth - MARGIN - 16, y + 5);
  doc.setTextColor(0, 0, 0);
  y += 12;

  // ─── Growth Indicators (Checklist) ───
  y = drawSectionHeader(doc, y, "GROWTH INDICATORS");

  fd.growthIndicators.forEach((indicator, idx) => {
    const lines = doc.splitTextToSize(indicator, contentWidth - 14);
    const rowH = lines.length * LINE_HEIGHT + 2;
    y = checkPageBreak(doc, y, rowH);

    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowH, "F");
    }

    doc.setDrawColor(180, 180, 180);
    doc.rect(MARGIN + 3, y + 0.5, 4, 4);

    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.text(lines, MARGIN + 10, y + 3);
    y += rowH;
  });
  y += 4;

  // ─── Reflection Prompts ───
  y = drawSectionHeader(doc, y, "REFLECTION PROMPTS");

  fd.reflectionPrompts.forEach((prompt, idx) => {
    const text = `${idx + 1}. ${prompt}`;
    const lines = doc.splitTextToSize(text, contentWidth - 8);
    y = checkPageBreak(doc, y, lines.length * LINE_HEIGHT + 20);
    doc.setFontSize(9);
    doc.setFont("helvetica", "normal");
    doc.text(lines, MARGIN + 3, y + 2);
    y += lines.length * LINE_HEIGHT + 3;
    // Blank lines for response
    y = drawBlankLines(doc, y, 3);
  });

  // ─── Teacher Comments ───
  y = drawSectionHeader(doc, y, "TEACHER COMMENTS");
  y = drawBlankLines(doc, y, 6);

  // ─── Signature Lines ───
  y = checkPageBreak(doc, y, 30);
  doc.setDrawColor(180, 180, 180);
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");

  doc.text("Teacher Signature:", MARGIN + 3, y);
  doc.line(MARGIN + 42, y, pageWidth / 2 - 5, y);
  doc.text("Date:", pageWidth / 2 + 10, y);
  doc.line(pageWidth / 2 + 23, y, pageWidth - MARGIN - 3, y);
  y += 10;

  doc.text("Parent / Guardian:", MARGIN + 3, y);
  doc.line(MARGIN + 42, y, pageWidth / 2 - 5, y);
  doc.text("Date:", pageWidth / 2 + 10, y);
  doc.line(pageWidth / 2 + 23, y, pageWidth - MARGIN - 3, y);
  y += 5;

  // ─── Footer ───
  drawFooter(doc, "Portfolio Assessment");

  return doc.output("arraybuffer");
}
