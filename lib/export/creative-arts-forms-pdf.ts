// ─── Style Constants ───

const HEADER_COLOR = [88, 28, 135] as const;
const SECTION_COLOR = [107, 33, 168] as const;
const TABLE_HEADER_BG = [243, 232, 255] as const;
const ALT_ROW_BG = [249, 250, 251] as const;
const LINE_HEIGHT = 5;
const MARGIN = 15;

// ─── Shared Helpers ───

function createDoc() {
  return new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
}

function getPageWidth(doc: jsPDF) {
  return doc.internal.pageSize.getWidth();
}

function getPageHeight(doc: jsPDF) {
  return doc.internal.pageSize.getHeight();
}

function getContentWidth(doc: jsPDF) {
  return getPageWidth(doc) - MARGIN * 2;
}

function checkPageBreak(doc: jsPDF, y: number, needed: number): number {
  if (y + needed > getPageHeight(doc) - MARGIN) {
    doc.addPage();
    return MARGIN;
  }
  return y;
}

function drawSectionHeader(
  doc: jsPDF,
  y: number,
  title: string
): number {
  const contentWidth = getContentWidth(doc);
  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...SECTION_COLOR);
  doc.rect(MARGIN, y, contentWidth, 7, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(10);
  doc.setFont("helvetica", "bold");
  doc.text(title, MARGIN + 3, y + 5);
  doc.setTextColor(0, 0, 0);
  return y + 9;
}

function drawFooter(doc: jsPDF, label: string) {
  const totalPages = doc.getNumberOfPages();
  const pageWidth = getPageWidth(doc);
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    doc.setFontSize(7);
    doc.setTextColor(150, 150, 150);
    doc.text(
      `Generated by MwalimuKit  --  ${label}  --  Page ${i} of ${totalPages}`,
      pageWidth / 2,
      getPageHeight(doc) - 8,
      { align: "center" }
    );
  }
}

function drawBlankLines(
  doc: jsPDF,
  y: number,
  count: number,
  label?: string
): number {
  const pageWidth = getPageWidth(doc);
  y = checkPageBreak(doc, y, count * 6 + (label ? 8 : 0));
  if (label) {
    doc.setFontSize(8);
    doc.setFont("helvetica", "italic");
    doc.setTextColor(120, 120, 120);
    doc.text(label, MARGIN + 3, y + 2);
    doc.setTextColor(0, 0, 0);
    y += 5;
  }
  for (let i = 0; i < count; i++) {
    y = checkPageBreak(doc, y, 6);
    doc.setDrawColor(200, 200, 200);
    doc.line(MARGIN + 3, y, pageWidth - MARGIN - 3, y);
    y += 6;
  }
  return y + 3;
}

function drawWrappedText(
  doc: jsPDF,
  y: number,
  text: string,
  fontSize = 9,
  fontStyle: "normal" | "bold" | "italic" = "normal"
): number {
  const contentWidth = getContentWidth(doc);
  doc.setFontSize(fontSize);
  doc.setFont("helvetica", fontStyle);
  const lines = doc.splitTextToSize(text, contentWidth - 6);
  let lineIdx = 0;
  while (lineIdx < lines.length) {
    const availableLines = Math.floor(
      (getPageHeight(doc) - MARGIN - y) / LINE_HEIGHT
    );
    const chunk = lines.slice(lineIdx, lineIdx + Math.max(1, availableLines));
    doc.text(chunk, MARGIN + 3, y + 2);
    y += chunk.length * LINE_HEIGHT + 2;
    lineIdx += chunk.length;
    if (lineIdx < lines.length) {
      doc.addPage();
      y = MARGIN;
    }
  }
  return y + 2;
}

