// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

export function generateAdjudicationFormPdf(
  data: AdjudicationExportData
): ArrayBuffer {
  const doc = createDoc();
  const pageWidth = getPageWidth(doc);
  const contentWidth = getContentWidth(doc);
  let y = MARGIN;

  // ─── School Name ───
  if (data.schoolName) {
    doc.setFontSize(11);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(40, 40, 40);
    doc.text(data.schoolName.toUpperCase(), pageWidth / 2, y, {
      align: "center",
    });
    y += 7;
  }

  // ─── Title ───
  doc.setFontSize(16);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("ADJUDICATION / SCORING FORM", pageWidth / 2, y, {
    align: "center",
  });
  doc.setTextColor(0, 0, 0);
  y += 6;

  // Subtitle (form name)
  doc.setFontSize(11);
  doc.setFont("helvetica", "normal");
  const titleLines = doc.splitTextToSize(data.formName, contentWidth);
  doc.text(titleLines, pageWidth / 2, y, { align: "center" });
  y += titleLines.length * 5 + 4;

  // ─── Event Info ───
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  if (data.formData.eventContext) {
    const ctxLines = doc.splitTextToSize(
      `Event: ${data.formData.eventContext}`,
      contentWidth - 6
    );
    doc.text(ctxLines, MARGIN + 3, y);
    y += ctxLines.length * LINE_HEIGHT + 1;
  }
  if (data.date) {
    doc.text(`Date: ${data.date}`, MARGIN + 3, y);
    y += 5;
  }
  doc.text(
    `Category: ${data.formData.category}  |  Grade: ${data.grade}  |  Discipline: ${data.artDiscipline}`,
    MARGIN + 3,
    y
  );
  y += 8;

  // ─── Blank Fields ───
  doc.setDrawColor(180, 180, 180);
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");

  doc.text("Performer / Group Name:", MARGIN + 3, y);
  doc.line(MARGIN + 50, y, pageWidth - MARGIN - 3, y);
  y += 7;

  doc.text("School / Institution:", MARGIN + 3, y);
  doc.line(MARGIN + 45, y, pageWidth - MARGIN - 3, y);
  y += 7;

  doc.text("Category Number:", MARGIN + 3, y);
  doc.line(MARGIN + 40, y, pageWidth - MARGIN - 3, y);
  y += 10;

  // ─── Judging Criteria Table ───
  y = drawSectionHeader(doc, y, "JUDGING CRITERIA");

  // Table header
  const colCriterion = MARGIN;
  const colDesc = MARGIN + 50;
  const colMax = pageWidth - MARGIN - 30;
  const colScore = pageWidth - MARGIN - 15;

  y = checkPageBreak(doc, y, 10);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(MARGIN, y, contentWidth, 7, "F");
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("Criterion", colCriterion + 2, y + 5);
  doc.text("Description", colDesc + 2, y + 5);
  doc.text("Max", colMax + 2, y + 5);
  doc.text("Score", colScore + 2, y + 5);
  doc.setTextColor(0, 0, 0);
  y += 9;

  // Table rows
  data.formData.judgingCriteria.forEach((crit, idx) => {
    const descLines = doc.splitTextToSize(
      crit.description,
      colMax - colDesc - 6
    );
    const critLines = doc.splitTextToSize(crit.criterion, 46);
    const rowHeight = Math.max(critLines.length, descLines.length) * LINE_HEIGHT + 4;

    y = checkPageBreak(doc, y, rowHeight);

    // Alternate row background
    if (idx % 2 === 0) {
      doc.setFillColor(...ALT_ROW_BG);
      doc.rect(MARGIN, y - 1, contentWidth, rowHeight, "F");
    }

    doc.setFontSize(8);
    doc.setFont("helvetica", "bold");
    doc.text(critLines, colCriterion + 2, y + 3);
    doc.setFont("helvetica", "normal");
    doc.text(descLines, colDesc + 2, y + 3);
    doc.text(String(crit.maxScore), colMax + 4, y + 3);

    // Score box
    doc.setDrawColor(180, 180, 180);
    doc.rect(colScore, y, 14, rowHeight - 2);

    y += rowHeight;
  });
  y += 4;

  // ─── Comment Sections ───
  data.formData.commentSections.forEach((section) => {
    y = drawSectionHeader(doc, y, section.label.toUpperCase());
    doc.setFontSize(8);
    doc.setFont("helvetica", "italic");
    doc.setTextColor(120, 120, 120);
    doc.text(section.purpose, MARGIN + 3, y + 1);
    doc.setTextColor(0, 0, 0);
    y += 5;
    y = drawBlankLines(doc, y, 4);
  });

  // ─── Total Score ───
  y = checkPageBreak(doc, y, 18);
  doc.setFillColor(...TABLE_HEADER_BG);
  doc.rect(pageWidth - MARGIN - 60, y, 60, 10, "F");
  doc.setFontSize(10);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...HEADER_COLOR);
  doc.text("TOTAL SCORE:", pageWidth - MARGIN - 58, y + 7);
  doc.setDrawColor(...SECTION_COLOR);
  doc.rect(pageWidth - MARGIN - 18, y + 1, 16, 8);
  doc.setFontSize(8);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(120, 120, 120);
  doc.text(`/ ${data.formData.totalPossibleScore}`, pageWidth - MARGIN - 16, y + 6);
  doc.setTextColor(0, 0, 0);
  y += 16;

  // ─── Adjudicator Signature ───
  y = checkPageBreak(doc, y, 20);
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  doc.setDrawColor(180, 180, 180);

  doc.text("Adjudicator Name:", MARGIN + 3, y);
  doc.line(MARGIN + 42, y, pageWidth / 2 - 5, y);
  doc.text("Signature:", pageWidth / 2 + 5, y);
  doc.line(pageWidth / 2 + 28, y, pageWidth - MARGIN - 3, y);
  y += 8;

  doc.text("Date:", MARGIN + 3, y);
  doc.line(MARGIN + 18, y, MARGIN + 60, y);
  y += 5;

  // ─── Footer ───
  drawFooter(doc, "Adjudication Form");

  return doc.output("arraybuffer");
}

